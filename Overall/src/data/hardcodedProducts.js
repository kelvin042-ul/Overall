// Function to get products by category
export const getProductsByCategory = (category) => {
    if (category === 'All') return hardcodedProducts;
    return hardcodedProducts.filter(product => product.category === category);
};

// Function to get products by subcategory
export const getProductsBySubcategory = (category, subcategory) => {
    return hardcodedProducts.filter(product =>
        product.category === category && product.subcategory === subcategory
    );
};

// Function to get a single product by ID
export const getProductById = (id) => {
    return hardcodedProducts.find(product => product.id === parseInt(id));
};

// Function to search products
export const searchProducts = (query) => {
    const searchTerm = query.toLowerCase();
    return hardcodedProducts.filter(product =>
        product.name.toLowerCase().includes(searchTerm) ||
        product.description.toLowerCase().includes(searchTerm) ||
        product.category.toLowerCase().includes(searchTerm)
    );
};

// Default export
export default hardcodedProducts;